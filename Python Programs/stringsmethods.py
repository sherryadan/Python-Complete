
a = "Adan"
print(a.upper())  #converts to uppercase
print(a.lower())  #converts to lowercase
print(a.title())  #converts first letter of each word to uppercase
print(a.capitalize())  #converts first letter to uppercase
print(a.swapcase())  #converts uppercase to lowercase and vice versa
print(a.count("a"))  #counts number of occurences of "a" in the string
print(a.find("a"))  #returns the index of the first occurence of "a" in the string
print(a.rfind("a"))  #returns the index of the last occurence of "a" in the string
print(a.replace("a", "e"))  #replaces all occurences of "a" with "e" in the string
print(a.startswith("A"))  #returns True if the string starts with "A"
print(a.endswith("n"))  #returns True if the string ends with "n"
print(a.isalpha())  #returns True if the string only contains alphabets
print(a.isalnum())  #returns True if the string only contains alphabets and numbers
print(a.islower())  #returns True if the string only contains lowercase alphabets
print(a.isupper())  #returns True if the string only contains uppercase alphabets
print(a.isspace())  #returns True if the string contains only spaces
print(a.istitle())  #returns True if the string contains only uppercase alphabets
print(a.isnumeric())  #returns True if the string only contains numbers
print(a.isdecimal())  #returns True if the string only contains decimals
